    private void cargar() {
        int v1 = Integer.parseInt(this.txtValor1.getText());
        int v2 = Integer.parseInt(this.txtValor2.getText());
        
        String total = "";
        
        for (int i = v1; i <= v2; i++) {
            //this.txtResultado.append(i + "\n");
            //this.txtResultado.append(i + " | ");
            total = total + " | " + i;
        }
        this.txtResultado.setText(total);
    }

