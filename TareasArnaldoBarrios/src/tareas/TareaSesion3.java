    private void cargarCampos() {
        //Obtenemos el texto de los campos de texto.
        String campo1 = this.txtCampo1.getText();
        String campo2 = this.txtCampo2.getText();
        String campo3 = this.txtCampo3.getText();
        String campo4 = this.txtCampo4.getText();
        String campo5 = this.txtCampo5.getText();

        //Creamos un arreglo y asignamos los valores de los campos.
        String[] campos = new String[]{campo1, campo2, campo3, campo4, campo5};

        //Creamos el modelo para asignarlo a nuestro Combo Box.
        DefaultComboBoxModel modelo = new DefaultComboBoxModel(campos);

        //Asignamos nuestro modelo a nuestro Combo Box.
        this.cbxCamposCargados.setModel(modelo);
        
        //Limpiamos los campos.
        this.limpiarCampos();
        
    }
    
    private void limpiarCampos() {
        this.txtCampo1.setText("");
        this.txtCampo2.setText("");
        this.txtCampo3.setText("");
        this.txtCampo4.setText("");
        this.txtCampo5.setText("");
        this.cbxCamposCargados.setSelectedIndex(0);
    }

